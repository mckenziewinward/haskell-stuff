type API = "position" :> Capture "x" Int :> Capture "y" Int :> Get '[JSON] Position
      :<|> "hello" :> QueryParam "name" String :> Get '[JSON] HelloMessage
      :<|> "marketing" :> ReqBody '[JSON] ClientInfo :> Post '[JSON] Email

server :: Server API
server = position
        :<|> hello
        :<|> marketing

userAPI :: Proxy API
userAPI = Proxy 

app :: Application
app = serve userAPI server

main :: IO ()
main = run 8081 app 